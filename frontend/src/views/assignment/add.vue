                      :modelValue="data.modelValue"
                      @searchSelect="
                        async (value) => (
                          await refresh(),
                          (cycles.cycle = cycles.cycle.filter((value1, index) => {
                            console.log(value1, value);
                            return value1.name.includes(value) || value.length == 0;
                          })),
                          console.log('searchSlect', value.length)
